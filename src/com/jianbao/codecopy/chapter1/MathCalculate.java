package com.jianbao.codecopy.chapter1;

/**
 * 本类主要实现基于Math类的数学运算
 */
public class MathCalculate {
    public static void main(String[] args) throws Exception{
        System.out.println("【绝对值】"+Math.abs(-10.3));
        System.out.println("【最大值】"+Math.max(10,20));
        System.out.println("【最小值】"+Math.min(10,30));
        System.out.println("【正弦值】"+Math.sin(3.1415));
        System.out.println("【对数值】"+Math.log(20));
    }
}
